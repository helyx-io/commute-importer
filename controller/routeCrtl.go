package controller

////////////////////////////////////////////////////////////////////////////////////////////////
/// Imports
////////////////////////////////////////////////////////////////////////////////////////////////

import (
	"net/http"
	"github.com/gorilla/mux"
	"github.com/helyx-io/gtfs-playground/database"
	"github.com/helyx-io/gtfs-playground/utils"
	"github.com/helyx-io/gtfs-playground/config"
)


////////////////////////////////////////////////////////////////////////////////////////////////
/// Structures
////////////////////////////////////////////////////////////////////////////////////////////////

type RouteController struct { }


////////////////////////////////////////////////////////////////////////////////////////////////
/// Variables
////////////////////////////////////////////////////////////////////////////////////////////////

var (
	routeRepository database.GTFSRouteRepository
)


////////////////////////////////////////////////////////////////////////////////////////////////
/// Route Controller
////////////////////////////////////////////////////////////////////////////////////////////////

func (routeController *RouteController) Init(r *mux.Router) {
	routeRepository = config.GTFS.Routes().(database.GTFSRouteRepository)

	r.HandleFunc("/", routeController.Routes)
}

func (ac *RouteController) Routes(w http.ResponseWriter, r *http.Request) {
	routes, err := routeRepository.FindAll()

	if err != nil {
		http.Error(w, err.Error(), 500)
	} else if routes == nil {
		http.Error(w, "No route found", 500)
	} else {
		utils.SendJSON(w, routes.ToJSONRoutes())
	}
}

