package controller

////////////////////////////////////////////////////////////////////////////////////////////////
/// Imports
////////////////////////////////////////////////////////////////////////////////////////////////

import (
	"net/http"
	"github.com/gorilla/mux"
	"github.com/helyx-io/gtfs-playground/database"
	"github.com/helyx-io/gtfs-playground/utils"
	"github.com/helyx-io/gtfs-playground/config"
)


////////////////////////////////////////////////////////////////////////////////////////////////
/// Structures
////////////////////////////////////////////////////////////////////////////////////////////////

type TripController struct { }


////////////////////////////////////////////////////////////////////////////////////////////////
/// Variables
////////////////////////////////////////////////////////////////////////////////////////////////

var (
	tripRepository database.GTFSTripRepository
)


////////////////////////////////////////////////////////////////////////////////////////////////
/// Trip Controller
////////////////////////////////////////////////////////////////////////////////////////////////

func (tripController *TripController) Init(r *mux.Router) {
	tripRepository = config.GTFS.Trips().(database.GTFSTripRepository)

	r.HandleFunc("/", tripController.Trips)
}

func (ac *TripController) Trips(w http.ResponseWriter, r *http.Request) {
	trips, err := tripRepository.FindAll()

	if err != nil {
		http.Error(w, err.Error(), 500)
	} else if trips == nil {
		http.Error(w, "No trip found", 500)
	} else {
		utils.SendJSON(w, trips.ToJSONTrips())
	}
}

