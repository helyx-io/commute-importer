package controller

////////////////////////////////////////////////////////////////////////////////////////////////
/// Imports
////////////////////////////////////////////////////////////////////////////////////////////////

import (
	"net/http"
	"github.com/gorilla/mux"
	"github.com/helyx-io/gtfs-playground/database"
	"github.com/helyx-io/gtfs-playground/utils"
	"github.com/helyx-io/gtfs-playground/config"
)


////////////////////////////////////////////////////////////////////////////////////////////////
/// Structures
////////////////////////////////////////////////////////////////////////////////////////////////

type TransferController struct { }


////////////////////////////////////////////////////////////////////////////////////////////////
/// Variables
////////////////////////////////////////////////////////////////////////////////////////////////

var (
	transferRepository database.GTFSTransferRepository
)


////////////////////////////////////////////////////////////////////////////////////////////////
/// Transfer Controller
////////////////////////////////////////////////////////////////////////////////////////////////

func (transferController *TransferController) Init(r *mux.Router) {
	transferRepository = config.GTFS.Transfers().(database.GTFSTransferRepository)

	r.HandleFunc("/", transferController.Transfers)
}

func (ac *TransferController) Transfers(w http.ResponseWriter, r *http.Request) {
	transfers, err := transferRepository.FindAll()

	if err != nil {
		http.Error(w, err.Error(), 500)
	} else if transfers == nil {
		http.Error(w, "No transfer found", 500)
	} else {
		utils.SendJSON(w, transfers.ToJSONTransfers())
	}
}

